#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { readYaml } from './lib/yaml-lite.mjs';
import { ensureDir, nowIso, writeJson } from './lib/project.mjs';

const root = process.cwd();
const employeeConfig = readYaml(path.join(root, 'config/employee.yaml'));
const modelConfig = readYaml(path.join(root, 'config/models.yaml'));
const mcpConfig = readYaml(path.join(root, 'config/mcp.yaml'));

function asRecord(value) {
  if (value && typeof value === 'object' && !Array.isArray(value)) return value;
  return {};
}

const employee = employeeConfig.employee || {};
const defaultProfile = employee.default_model_profile || modelConfig.default_profile || '';

const agent = {
  schema_version: 'employee-agent.v1',
  employee_id: employee.id || 'employee-template',
  name: employee.name || 'Template Employee',
  level: employee.level || 'junior',
  role: employee.role || 'implementation_employee',
  default_model_profile: defaultProfile,
  capabilities: employeeConfig.capabilities || [],
  accepts_task_types: employeeConfig.accepts_task_types || [],
  paths: {
    inbox: 'inbox/tasks',
    outbox: 'outbox/results',
    status: 'state/status.json',
    events: 'logs/events.jsonl'
  }
};

writeJson(path.join(root, 'agent.json'), agent);

const statusPath = path.join(root, 'state/status.json');
if (!fs.existsSync(statusPath)) {
  writeJson(statusPath, {
    schema_version: 'employee-status.v1',
    employee_id: agent.employee_id,
    state: 'idle',
    active_task_id: null,
    model_profile: defaultProfile,
    updated_at: nowIso()
  });
}

writeJson(path.join(root, 'state/status.example.json'), {
  schema_version: 'employee-status.v1',
  employee_id: agent.employee_id,
  state: 'idle',
  active_task_id: null,
  model_profile: defaultProfile,
  updated_at: ''
});

writeJson(path.join(root, '.mcp.json'), {
  mcpServers: asRecord(mcpConfig.mcp_servers)
});

writeJson(path.join(root, '.claude/settings.json'), {
  permissions: {
    defaultMode: 'acceptEdits',
    allow: [
      'Read',
      'Write(outbox/**)',
      'Write(state/**)',
      'Write(logs/**)',
      'Write(memory/**)',
      'Write(workspace/**)',
      'Edit(outbox/**)',
      'Edit(state/**)',
      'Edit(logs/**)',
      'Edit(memory/**)',
      'Edit(workspace/**)',
      'Bash(npm run doctor)',
      'Bash(npm run validate)',
      'Bash(npm run sync)',
      'Bash(git status:*)',
      'Bash(git diff:*)',
      'Bash(rg:*)',
      'Bash(find:*)',
      'Bash(ls:*)',
      'Bash(cat:*)',
      'Bash(sed:*)',
      'Bash(tail:*)'
    ],
    deny: [
      'Read(./.env)',
      'Read(./.env.*)',
      'Write(./.env)',
      'Write(./.env.*)',
      'Edit(./.env)',
      'Edit(./.env.*)'
    ]
  }
});

try {
  ensureDir(path.join(root, '.codex'));
  fs.writeFileSync(path.join(root, '.codex/AGENTS.md'), [
    '# Codex Adapter',
    '',
    'This file is generated by `npm run sync`.',
    '',
    'Read `../AGENTS.md` and use the JSON bridge described in `../agent.json`.',
    ''
  ].join('\n'));
} catch (error) {
  console.warn(`warning - skipped optional .codex adapter: ${error.message}`);
}

for (const dir of [
  'inbox/tasks',
  'outbox/results',
  'workspace',
  'memory',
  'logs',
  'state',
  '.claude/commands',
  '.claude/skills/base',
  '.claude/skills/task-execution'
]) {
  ensureDir(path.join(root, dir));
}

console.log('synced project adapters');
